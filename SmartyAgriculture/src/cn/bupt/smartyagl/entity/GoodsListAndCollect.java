package cn.bupt.smartyagl.entity;

import cn.bupt.smartyagl.entity.autogenerate.GoodsList;

/** 
 * 带是否收藏字段的商品详情Bean
 * @author  jm E-mail:740869614@qq.com 
 * @date 创建时间：2016-6-20 上午9:25:19 
 * @version 1.0 
 * @parameter  
 * @since  
 * @return  
 */
public class GoodsListAndCollect extends GoodsList{
	/**
	 * 是否收藏
	 */
	private  boolean isCollect;
	/**
	 * 随机一条评论
	 */
	private Object comment;
	/**
	 * 评论数量
	 */
	private Integer commentNum;
	/**
	 * 图片处理
	 */
	private String goodsDetailHTML;
	private String qrcodeUrl;

	public String getQrcodeUrl() {
		return qrcodeUrl;
	}

	public void setQrcodeUrl(String qrcodeUrl) {
		this.qrcodeUrl = qrcodeUrl;
	}
	public String getGoodsDetailHTML() {
		return goodsDetailHTML;
	}

	public void setGoodsDetailHTML(String goodsDetailHTML) {
		this.goodsDetailHTML = goodsDetailHTML;
	}

	public boolean isCollect() {
		return isCollect;
	}

	public void setCollect(boolean isCollect) {
		this.isCollect = isCollect;
	}

	public Object getComment() {
		return comment;
	}

	public void setComment(Object comment) {
		this.comment = comment;
	}

	public Integer getCommentNum() {
		return commentNum;
	}

	public void setCommentNum(Integer commentNum) {
		this.commentNum = commentNum;
	}
	

}
